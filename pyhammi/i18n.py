def set_language(lang: str) -> None:
    global _CURRENT
    _CURRENT = "zh" if lang == "zh" else "en"


def get_language() -> str:
    return _CURRENT


def t(key: str, **kwargs) -> str:
    src = _TR_ZH if _CURRENT == "zh" else _TR_EN
    text = src.get(key, _TR_EN.get(key, key))
    if kwargs:
        text = text.format(**kwargs)
    return text
